def compare_values(a: int, b: int) -> None:
    """
    Compare two values and print whether they are equal or not.
    
    Args:
    a (int): The first value to compare.
    b (int): The second value to compare.
    """
    if a == b:
        print("equal")
    else:
        print("not equal")

compare_values(1, 2)