def my_function(x: int, y: int) -> int:
    """
    This function adds two numbers and returns the result.
    
    Args:
        x (int): The first number.
        y (int): The second number.
    
    Returns:
        int: The sum of x and y.
    """
    result = x + y
    print(result)
    return result

my_function(10, 20)